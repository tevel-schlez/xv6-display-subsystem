// ── User-space pixel helpers ──────────────────────────────────────────
// fb is the pointer returned by map_display() — writes go directly to the
// kernel's fb[] physical pages with no syscall.

static void
putpixel(uint32 *fb, int x, int y, uint32 color)
{
    fb[y * SCREEN_W + x] = color;
}

// Draw one 8×8 glyph scaled SCALE×SCALE at cell position (cx, cy).
static void
draw_char(uint32 *fb, int cx, int cy, unsigned char ch)
{
    if (ch < 0x20 || ch >= 0x7F)
        ch = '?';
    const uint8 *rows = font8x8[ch];
    for (int row = 0; row < 8; row++)
    {
        for (int col = 0; col < 8; col++)
        {
            uint32 color = (rows[row] & (1u << col)) ? COLOR_FG : COLOR_BG;
            for (int dy = 0; dy < SCALE; dy++)
                for (int dx = 0; dx < SCALE; dx++)
                    putpixel(fb,
                             cx + col * SCALE + dx,
                             cy + row * SCALE + dy,
                             color);
        }
    }
}

// Render a string centred horizontally and vertically on the screen.
// Truncates to the maximum number of glyphs that fit on one line.
static void
render_centred(uint32 *fb, const char *text, int len)
{
    int char_w = 8 * SCALE;
    int char_h = 8 * SCALE;
    int max_chars = SCREEN_W / char_w;

    if (len > max_chars)
        len = max_chars;

    int x0 = (SCREEN_W - len * char_w) / 2;
    int y0 = (SCREEN_H - char_h) / 2;

    for (int i = 0; i < len; i++)
        draw_char(fb, x0 + i * char_w, y0, (unsigned char)text[i]);
}

// ── main ──────────────────────────────────────────────────────────────
int main(void)
{
    // ── 1. Map GPU framebuffer into our address space ─────────────────
    // Pass an explicit page-aligned VA well above the heap. The kernel
    // installs PTEs at this VA pointing at the GPU's physical fb[] pages
    // (PTE_U|PTE_R|PTE_W).  No pixel data is moved.
    //
    // Passing 0 instead would let the kernel auto-pick a free VA.
    void *want_addr = (void *)0x10000000;
    uint32 *fb = (uint32 *)map_display(want_addr);
    if (fb == (uint32 *)(uint64)-1 || fb == 0)
    {
        fprintf(2, "show_map: map_display failed\n");
        exit(1);
    }

    // ── 2. Initial clear ──────────────────────────────────────────────
    memset(fb, 0, FB_BYTES);
    // (daemon will flush within ~16 ms)

    printf("show_map: type text and press Enter to display it.\n");
    printf("          Type 'exit' to clear the screen and quit.\n");

    // ── 3. Interactive loop ───────────────────────────────────────────
    char line[256];
    while (1)
    {
        printf("> ");

        // gets() reads from stdin until '\n' or max-1 chars.
        if (gets(line, sizeof(line)) == 0)
            break; // EOF (e.g. stdin closed)

        // Strip trailing newline.
        int len = strlen(line);
        if (len > 0 && line[len - 1] == '\n')
            line[--len] = '\0';

        if (strcmp(line, "exit") == 0)
            break;

        // ── Render entirely in user space ─────────────────────────────
        memset(fb, 0, FB_BYTES);
        render_centred(fb, line, len);
        // (daemon flushes to display within ~16 ms)
    }

    // ── 4. Clear display before exit ──────────────────────────────────
    memset(fb, 0, FB_BYTES);

    exit(0);
}
