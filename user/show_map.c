//
// show_map.c – interactive display via memory-mapped GPU framebuffer.
//
// How it works:
//   1. map_display(addr) maps the kernel's GPU framebuffer pages into this
//      process's address space (PTE_U|PTE_R|PTE_W).  No pixel copy.
//        - addr == 0  : kernel auto-selects a free VA above p->sz.
//        - addr != 0  : kernel installs the mapping at the given
//                       page-aligned VA (must not collide).
//      This program demos the second form by passing an explicit VA.
//   2. The user process writes pixels directly through the returned
//      pointer.
//   3. The kernel display_daemon flushes the display every ~16 ms
//      (TRANSFER_TO_HOST_2D + RESOURCE_FLUSH).
//
// Usage:
//   show_map               (no arguments)
//   > Hello World          (type any text, press Enter)
//   > Foo                  (replaces previous text)
//   > exit                 (clears screen and quits)
//

